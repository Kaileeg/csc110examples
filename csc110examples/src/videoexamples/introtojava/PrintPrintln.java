package videoexamples.introtojava;

public class PrintPrintln {

	public static void main(String[] args) {
		
		System.out.print("System.");
		System.out.print("out.");
		System.out.print("print ");
		System.out.print("does not include a newline at the end of the output");
		System.out.print("\n");   // go to the next line
		System.out.print("But you can include a newline character (\\n) in the string \n");
		
		System.out.println();  // go to the next line (or print empty line)
		System.out.println("System.");
		System.out.println("out.");
		System.out.println("println");
		System.out.println("does include a newline at the end of the output");

	}

}
